        model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        if (Ar != null) {
            for (int i = 0; i < Ar[0].length; i++) {
                jComboBox1.addItem(Ar[4][i]);
                jComboBox2.addItem(Ar[6][i]);
                model.addRow(new Object[]{
                    Ar[0][i],
                    Ar[1][i],
                    Ar[2][i],
                    Ar[3][i],
                    Ar[4][i],
                    Ar[5][i],
                    Ar[6][i]});
            }
        }
    
